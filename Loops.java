import java.util.Scanner;

class Loops {
    
    public static void main(String argc[]) {
        
        Scanner scanner = new Scanner(System.in);
        int x = scanner.nextInt();
        switch(x) {
        
            case 0: 
                System.out.println("0");
                break;
            default:
                System.out.println("!0");
                break;
                
        }
        
    }
    
}